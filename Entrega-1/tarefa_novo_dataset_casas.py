import pandas as pd #Uma biblioteca do Python usada para trabalhar com tabelas

df = pd.read_csv("data.csv") #A função read_csv() lê o arquivo csv referênciado e transforma em um DataFrame
df.head() #Visualiza os dados, para ver se foi carregado corretamente e entender como os dados estão organizados

df.info() #Mostra quantas linhas existem, quais colunas existem, o tipo da coluna e se há valores vazios
df.describe() #Mostra a média, desvio padrão, valor minimo, valor máximo

#Correlação - É uma medida que diz o quanto duas variaveis estão relacionadas
#Aqui eu quero saber quais colunas tem mais relação com o preço
df.corr(numeric_only=True)["price"].sort_values(ascending=False)
#O sort_values(ascending=False) organiza do maior para o menor

#Nesse aqui defino para o modelo que ele usará essas colunas para tentar prever o preço da casa
X = df[[
    "bedrooms", #quartos
    "bathrooms", #banheiros
    "sqft_living", #sqft_living
    "floors", #número de andares
    "waterfront", #se é de frente para água
    "view", #qualidade da vista
    "condition", #condição da casa
    "sqft_above", #área acima do solo
    "sqft_basement", #área do porão
    "yr_built" #ano de construção
]] # X = caractersticas (features)

y = df["price"] #Y = variavel alvo (target)

from sklearn.linear_model import LinearRegression #Importando a ferramenta de regressão linear da biblioteca scikit-learn
modelo = LinearRegression() #Aqui crio um modelo vazio
modelo.fit(X, y) #Aqui acontece o aprendizado do modelo, ele analisa os dados, calcula a melhor combinação matemática e encontra os coeficientes. É como dizer "Modelo, observe esses dados e aprenda a relação entre X e Y"

for nome, coef in zip(X.columns, modelo.coef_): #Percorre o nome da coluna e o valor do coeficiente. O modelo.coef_ vai retornar vários números: "preç​o=a1×bedrooms+a2×bathrooms+a3×sqftl​iving+...+b", cada "a" é um coeficiente. Peso/importância de cada variável. Quanto aquela variável adiciona.
    print(f"{nome}: {coef:.2f}") #O valor que der, representa que, a cada aumento de um pé quadrado (1 unidade de tamanho), aumenta o preço em tantos dolares

print("Intercept:", modelo.intercept_) #Valor onde a reta corta o eixo Y

y_pred = modelo.predict(X) #Aqui o modelo pega os dados e calcula os preços previstos aplicando a formula que ele aprendeu. É como dizer: "Modelo, agora use o que você aprendeu para calcular um novo preço"

#Aqui é criado uma nova tabela a onde terá as colunas preço real e preço previsto
resultado = X.copy()
resultado["Preço Real"] = y
resultado["Preço Previsto"] = y_pred

resultado.head()

#Aqui crio uma nova casa de forma manual
nova_casa = [[
    3,      # bedrooms
    2,      # bathrooms
    1800,   # sqft_living
    2,      # floors
    0,      # waterfront
    1,      # view
    3,      # condition
    1800,   # sqft_above
    0,      # sqft_basement
    2010    # yr_built
]]

# O modelo aplica a formula que aprendeu e retorna o preço estimado da casa
preco_previsto = modelo.predict(nova_casa)

print(f"Preço previsto: {preco_previsto[0]:.2f}")